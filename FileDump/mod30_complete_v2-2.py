def skew_angle(quark_list):
    """
    Deviation of three quark angles from perfect 120deg spacing
    on the 720deg spinor wheel.

    Perfect equilateral triangle: 240deg between each quark.
    Skew = mean absolute deviation from 240deg across all pairwise gaps.

    For identical quarks: skew encodes the triangular toroid structure.
    For mixed quarks: skew measures geometric stress / quasi-moto emission.

    The quasi-moto particle mass scales as:
      m_quasi ~ skew_angle * cycle_class_fraction * geometric_constant
    """
    angles = sorted([ANGLES[ASSIGNMENT[q]] for q in quark_list])
    gaps = []
    n = len(angles)
    for i in range(n):
        for j in range(i+1, n):
            gaps.append(abs(angles[j] - angles[i]))
    gaps.append(720 - angles[-1] + angles[0])
    deviations = [abs(g - 240) for g in gaps]
    return np.mean(deviations)

def cycle_class_factor(quark_list):
    """Average cycle class fraction for the quark list."""
    return np.mean([CYCLE_FRAC[CYCLE_CLASS[q]] for q in quark_list])

